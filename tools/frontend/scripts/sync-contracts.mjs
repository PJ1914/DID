#!/usr/bin/env node

import { promises as fs } from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const repoRoot = path.resolve(__dirname, '../../..');
const abiOutputDir = path.resolve(__dirname, '../src/lib/abis');

const CONTRACTS = [
    {
        name: 'IdentityRegistry',
        artifactPath: path.join(repoRoot, 'out', 'IdentityRegistry.sol', 'IdentityRegistry.json'),
        exportName: 'identityRegistryAbi',
        outputFile: 'identityRegistry.ts'
    },
    {
        name: 'TrustScore',
        artifactPath: path.join(repoRoot, 'out', 'TrustScore.sol', 'TrustScore.json'),
        exportName: 'trustScoreAbi',
        outputFile: 'trustScore.ts'
    },
    {
        name: 'VerificationManager',
        artifactPath: path.join(repoRoot, 'out', 'VerificationManager.sol', 'VerificationManager.json'),
        exportName: 'verificationManagerAbi',
        outputFile: 'verificationManager.ts'
    },
    {
        name: 'VerificationLogger',
        artifactPath: path.join(repoRoot, 'out', 'VerificationLogger.sol', 'VerificationLogger.json'),
        exportName: 'verificationLoggerAbi',
        outputFile: 'verificationLogger.ts'
    },
    {
        name: 'OrganizationManager',
        artifactPath: path.join(repoRoot, 'out', 'OrganizationManager.sol', 'OrganizationManager.json'),
        exportName: 'organizationManagerAbi',
        outputFile: 'organizationManager.ts'
    },
    {
        name: 'ZKProofManager',
        artifactPath: path.join(repoRoot, 'out', 'ZKProofManager.sol', 'ZKProofManager.json'),
        exportName: 'zkProofManagerAbi',
        outputFile: 'zkProofManager.ts'
    }
];

const FILE_HEADER = `/**\n * Autogenerated by scripts/sync-contracts.mjs.\n * Do not edit manually.\n */\n\n`;

function formatAbi(abi) {
    return JSON.stringify(abi, null, 4);
}

async function ensureDirectory(dir) {
    try {
        await fs.mkdir(dir, { recursive: true });
    } catch (error) {
        if (error.code !== 'EEXIST') {
            throw error;
        }
    }
}

async function readArtifact(contract) {
    try {
        const contents = await fs.readFile(contract.artifactPath, 'utf8');
        return JSON.parse(contents);
    } catch (error) {
        if (error.code === 'ENOENT') {
            throw new Error(
                `Artifact for ${contract.name} not found at ${contract.artifactPath}. Did you run \"forge build\"?`
            );
        }
        throw error;
    }
}

async function writeAbiFile(contract, abi) {
    const formattedAbi = formatAbi(abi);
    const fileContents = `${FILE_HEADER}export const ${contract.exportName} = ${formattedAbi} as const;\n`;
    const outputPath = path.join(abiOutputDir, contract.outputFile);
    await fs.writeFile(outputPath, fileContents, 'utf8');
    return outputPath;
}

async function generateIndexFile() {
    const lines = CONTRACTS.map(
        (contract) => `export { ${contract.exportName} } from './${contract.outputFile.replace(/\.ts$/, '')}';`
    );
    const contents = `${FILE_HEADER}${lines.join('\n')}\n`;
    const indexPath = path.join(abiOutputDir, 'index.ts');
    await fs.writeFile(indexPath, contents, 'utf8');
    return indexPath;
}

async function main() {
    await ensureDirectory(abiOutputDir);

    const generatedFiles = [];

    for (const contract of CONTRACTS) {
        const artifact = await readArtifact(contract);
        if (!artifact.abi) {
            throw new Error(`Artifact for ${contract.name} does not contain an ABI.`);
        }
        const outputPath = await writeAbiFile(contract, artifact.abi);
        generatedFiles.push({ contract: contract.name, outputPath });
    }

    const indexPath = await generateIndexFile();

    console.log('✅ Synced ABIs for:');
    for (const { contract, outputPath } of generatedFiles) {
        console.log(`  - ${contract} → ${path.relative(repoRoot, outputPath)}`);
    }
    console.log(`  - index → ${path.relative(repoRoot, indexPath)}`);
}

main().catch((error) => {
    console.error('Failed to sync contract ABIs:', error.message);
    process.exitCode = 1;
});
